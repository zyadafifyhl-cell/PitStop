export function translate(locale: Locale, key: TranslationKey): string {
  return locale === 'ar' ? AR[key] : EN[key];
}

export function tp(locale: Locale, key: TranslationKey, vars: Record<string, string>): string {
  let s = translate(locale, key);
  for (const [k, v] of Object.entries(vars)) {
    s = s.split(`{${k}}`).join(v);
  }
  return s;
}
